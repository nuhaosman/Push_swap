#include "ft_is.h"

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}

int	ft_isdigit_char(char c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
